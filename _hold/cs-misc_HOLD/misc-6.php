    <div class="content-arti-copy">
        <p>As a part of the startup for this venture, I was asked to design box and tin packaging at a number of sizes for a roasted rice tea. I was provided with box dimensions from the manufacturer by way of outlines for each of the packaging types, nutrition information and ingredients, a URL that they wanted included via QR code, and a quick run-down of what the product being sold was.</p>
        <p>As the client wanted to market the tea for the health properties of each's core ingredients, I started by drawing and vectorising the 3 core ingredients for each of the 3 tea flavours that would eventually be sold. These were a stalk of rice, a sprig of rosemary, and a piece of ginger. Each would serve as the central design feature of each packaging size.</p>
        <p>Following this, I had the URLs converted into QR codes, and chose base colours for each of the flavours. For the roasted rice tea, I chose a deep red. As this would the be venture's first product, I decided that using the same red as was used in the logo would ensure that the venture's brand would be well represented.</p>
        <p>I chose simple design elements to build each packaging from, namely the cetnral graphic at a lowered opacity, and a white horizontal running across the whole packaging. This singular horizontal was chosen in order to offer grounding to the design, and to keep the brand perception simple.</p>
        <p>This design was then adapted to the various box and tin sizes. This was particularly challenging with the smaller boxes, as there were large blocks of information that needed to be included on each packaging, making keeping a simple design with a consistent amount of white space a challenge. This was largely overcome by concentrating information on the back of the packaging, and giving the front design room to breathe.</p>
        <p>As my first venture into package design, I found bringing all information into the page and keeping it readable the key challenge for me. From this project, I learned very quickly that there was a balance between necessary information and design that needed to be struck with package design, and that doing more with less is key.</p>
        <p>The core design elements for this project were drawn in <span class="bold">Adobe Illustrator</span> and laid out in <span class="bold">Adobe InDesign</span>.</p>
