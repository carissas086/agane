        <p>The site was designed to serve 2 functions:</p>
        <ul class="li-indent bullets">
            <li>showcase the artwork present in the current gallery, and</li>
            <li>archive artwork from previous galleries (as the gallery continued into future years)</li>
        </ul>
        <p>The showcase gallery was built using a combination of CSS and Javascript, and the site was set up to be achievable (and therefore filterable) using a jQuery plugin called 'Isotope'. Due to the community-centric nature of the ministry, the functionality to filter by artist was suggested and coded into the site as well.</p>
        <p>As we have only launched 1 gallery to date, the gallery archive has been commented out on the live site so as not to confuse users.</p>
