    <div class="content-arti-copy">
        <p>The challenge with the Trench Hall scene was that the main characters were purposely placed on opposite ends of the scene. Initial sketches of the post had me only featuring 1 of the 2 mains, however this felt as though I was directing the viewer to form a bias for 1 character over the other.</p>
