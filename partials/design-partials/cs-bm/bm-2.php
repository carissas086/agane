    <div class="content-arti-copy">
        <p>My solution was to create a split frame, with the female lead in the lighter left half of the post image, and the male lead in the shadows to the right. The left half of the frame was given a slant downwards, offering slight momentum in the horizontal while still grounding the image in the heavy emotion that the male lead shows.</p>
        <p>As the image is entirely black and white, there was a slight challenge in showing that the image was intentionally cut and rejoined as the grainy black and white style of the art obscured the split. To remedy this, the right image was masked with a gradient, and a black background laid behind the entire image. This created the effect of a drop shadow, working to generate more contrast between the 2 sides while casting a darker shadow on the male lead.</p>
