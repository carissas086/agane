<h2 class="head-font arti-head">Project Overview</h2>
<div class="content-arti-clean flex flex-end">
    <div class="content-arti-copy">
        <p>This project was completed as part of an internship with BCIT's Tech Collider. The space would be a hub for industry professionals and students training to join the tech industry to both showcase and learn about various technologies, such as <span class="bold">AR, VR, &amp; 3D modelling and printing</span>. The broader goal of the project was to create content for the purposes of marketing this new space. This would include:</p>
        <ul class="li-indent bullets">
            <li>a set of photographs to be used in digital marketing</li>
            <li><a href="https://www.youtube.com/watch?v=zGL-l4sZIJ4" target="_blank" title="Final promotional video created" class="art-link">a promotional video</a></li>
        </ul>
        <p>I worked with teammate <a href="https://eunhye-jang.ca" class="art-link" target="_black" title="Visit Eunhye's portfolio!">Eunhye Jang</a> as part of the graphics team for this project. My role was that of a <span class="bold">graphic design intern</span>, and primarily revolved around the creation of these assets.</p>
        <p>Due to the space's desire to promote new technologies, we decided to lean into the augmented and virtual reality tech that the space wanted to promote in all of our assets as a graphics team. This led us to develop a  set of 3D assets and a consistent shader that we could use to augment some of the photos, and motion graphics in the video.</p>
