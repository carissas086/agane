    <div class="content-arti-copy">
        <p>I had initially intended on attempting something more complex with the letterhead, taking the 2 negative space shapes of the outside of the eye and using them to frame all the contents, however found this relatively impractical. I was initially stubborn in keeping this broken apart shape on either sides of the letterhead. However, I found in the end that the logo was capable of standing on its own in the top left unimpeded, while the upwards curve generated good momentum and interest with the subdued yellow to leave the page feeling lighter, and the 2 circles from the logo floating in the shape to invoke that more playful sensibility at the bottom of the page.</p>
    </div>
</div>
<div class="content-arti-clean flex flip">
    <div class="content-arti-copy">
        <p>The envelope underwent a similar path to the business cards, leaning on the soft curves from the logo to frame information, and the colours and font choices to carry the meaning and feel of the envelope forward.</p>
