
<h2 class="head-font arti-head">Portrait of the Spaceman</h2>
<div class="content-arti-clean flex flex-around flex-end">
    <div class="content-arti-final framed">
        <a href="../images/cs_portrait-sketch.jpg"
            data-lightbox="misc-1"
            data-title="Initial sketch done in Photoshop of the composite photo I wanted to make. I chose to sketch in white on black in order to indicate to the subject, and as a guide to how I wanted to shoot the photos, how light would work in the piece." >
                <img src="../images/cs_portrait-sketch.jpg" alt="black and white sketch of composite photograph">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>The concept for this project began with a curious statement often made by <a href="https://roamresearch.com/#/app/JosephSo___Library/page/HsewQ0Z5j" target="_blank" title="Joseph's Public Library" class="art-link">Joseph So</a>. As a composer and music producer, Joseph (sometimes adopting the handle 'The Spaceman') would say that he's a musician, whose instrument is 'the computer'. My goal with this project, therefore, was to create a piece that would show what that looks like, to make 'a portrait of the Spaceman'.</p>
        <p>My first task was to sketch out what that would look like. In my initial sketch, I wanted to have him reaching out of one of his music projects for his computer keyboard, MIDI controller in one hand, and surrounded by the things that he often uses to make music. I presented this sketch to him, and got the go-ahead to plan the rest of the shoot.</p>
    </div>
</div>
<div class="content-arti-clean flex flip flex-end">
    <div class="content-arti-final framed">
        <a href="../images/cs_portrait-desktop.jpg"
            data-lightbox="misc-1"
            data-title="The final shot of Joseph's workstation that I chose to use as the background for the image. This was shot at ISO 200, f/22, with a 3s shutter." >
                <img src="../images/cs_portrait-desktop.jpg" alt="Computer monitor on a desk with a Macbook, Nintendo switch, mouse, and headphones nearby.">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>Once this was laid out, I did a few quick test shots in Joseph's workspace so that I could plan the shoot day. I noted first that his workspace was incredibly poorly lit, and would therefore need additional lighting, and a good tripod to keep the shots steady and consistent. I also noted that there were elements on his desk that were not necessaryily music production related, however very well encapsulated who he was as a creative mind. I took note of this, and told him in the end to simply have everything that was 'him' on his desk for shoot day.</p>
        <p>The plan for the shoot itself was split into 2 halves: workstation shots, and person shots. I had him lay out his workstation with an empty deskmat first, and took a series of panoramic shots in hopes of compiling them into a wider, higher resolution shot later. This wound up not panning out due to the poor lighting situation, and so the decision to take additional shots of just his desk proved to be incredibly useful.</p>
        <p>As this shoot wound up happening the week of the heatwave in Vancouver, Joseph had an airconditioner set up behind his desk. This would add an additional challenge later in compositing.</p>
    </div>
</div>
