<div class="content-arti-clean flex flex-around flex-end framed">
    <div class="content-arti-copy">
        <p>Next, I took a number of shots at varying angles of Joseph's effects board, and his hand-build 40% keyboard. Due to the poor lighting in his workspace, we had to move things around in order to ensure that we could get adequate lighting on his equipment.</p>
    </div>
</div>
<div class="content-arti-clean flex flex-end">
    <div class="content-arti-final framed">
        <a href="../images/cs_portrait-jo-2.jpg"
            data-lightbox="misc-1"
            data-title="The chosen shot of Joseph reaching out of his monitor. This would serve as the focal point of the composite. This was shot at ISO 800, f/22, with a 0.2s shutter." >
                <img src="../images/cs_portrait-jo-2.jpg" alt="Boy leaning over 2 mic stands, wearing a mask">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>Next, we took various shots of Joseph himself in the poses that I wanted for the composite. With these, I made an effort to ensure that he was as flatly lit as possible, as I wanted to be able to manipulate the lighting later in post (though this was not the best decision, in hindsight).</p>
        <p>The first was the main shot of him reaching out of his monitor. In order to simulate him reaching out of the monitor, we set up 2 mic stands for him to lean over. Following that were shots of him looking down over a monitor. For this shot, I draped a blue pillowcase over a mic stand in order to help the editing process later, since he was wearing red for that shot. The last shot wound up being of him lying down and playing a mobile game, which I had initially wanted to place on top of his Nintendo Switch.</p>
        <p>With these shots done, I was ready to head into post-production.</p>
    </div>
