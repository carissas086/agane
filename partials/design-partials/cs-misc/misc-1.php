    <div class="content-arti-copy">
        <p>The first step in post-production of the composite was choosing the shots I wanted to use, and cleaning them up to be composited together. I spent several days masking out each of the 6 shots, using a combination of the pen and lasso tools in Photoshop in order to get as clean a selection on each subject as possible. Once this was done, each were placed on the canvas as envisioned.</p>
        <p>This is where the main issue arose. As this shoot happened during an unprecedented heatwave, the entire back wall of Joseph's workstation had to be edited out. While I had initially wanted to simply remove the air conditioner duct, the colour of the wall did not contrast against his hair well, so I decided to set everything against black. Together with the extremely even lighting that I had employed, however, this made the entire shot look unbelievable, and bland.</p>
    </div>
</div>
<div class="content-arti-clean flex flex-end">
    <div class="content-arti-final framed">
        <a href="../images/cs_portrait-comp-2.jpg"
            data-lightbox="misc-1"
            data-title="Second edit of the composite with initial lighting and fog edits." >
                <img src="../images/cs_portrait-comp-2.jpg" alt="Boy reaching out of monitor, with boy playing a mobile game, and boy looking at a video of himself nearby">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>The first set of edits I did were aimed at adding some environmental fog between every of the 6 shots. These were all hand painted using the brush tool and blur layer effects in Photoshop. These fog layers were masked in order to blend them together as naturally as possible. This served to add more interest and depth to the various layers, and deemphasise the flat black background.</p>
        <p>Also in this set of edits were lighting adjustments made. This was accomplished by painting light in on their own layers, and clipping them to the various photo isolations. This way of working allowed me to work non-destructively, and build up a more natural light scatter pattern throughout the piece. It also allowed me to emphasise the main focal point of the photo.</p>
    </div>
</div>
<div class="content-arti-clean flex flex-end">
    <div class="content-arti-copy">
        <p>Thereafter, I painted in a light beam that would cut across the focal subject's hand, and applied more fog layers around this beam in order to give it a more ethereal look. In order to make the beam more believable, I added in dust particles as well.</p>
        <p>Finally, I applied a series of adjustments to the entire image, pushing the board and the laptop a little further back into the frame, and sharpening the image slightly. Resulting in the final image:</p>
