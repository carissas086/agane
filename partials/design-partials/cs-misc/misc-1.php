        <p>This project taught me that while planning was important, being willing to pivot, and keep an open mind to better opportunities was also very important. While not exactly as initially sketched, by keeping the idea that I wanted to portray in mind first, rather than the checklist of things to do, I was able to produce a true portrait as I had set out to do.</p>
