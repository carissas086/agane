    <div class="content-arti-copy">
        <p>The goal for this project was to create a photo-realistic illustration of an object using vectors only. I chose to recreate an effect pedal by Chase Bliss Audio (first pictured below), as it served as an homage to prior education, and the intricacies of the numerous shiny switches and knobs would serve to show my abilities with Adobe Illustrator.</p>
        <p>The recreation was done using only vector shapes, and various gradients applied to the shape fills and strokes in order to layer in the nuanced colour present in the product photograph.</p>
        <h3 class="bold">Tools Utilised</h3>
        <ul class="li-indent bullets">
            <li>Adobe Illustrator</li>
        </ul>
