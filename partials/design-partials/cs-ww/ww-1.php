        <p>I was asked to create a number of small animated graphics for <a href="https://roamresearch.com/#/app/JosephSo___Library/page/HsewQ0Z5j" target="_blank" title="Joseph's Public Library" class="art-link">Joseph So</a>'s run-down of his mother's day worship set at Fraserlands Church. This bridge animation was conceptualised as a visual explaination to illustrate the connection between the 2 sections of music, with the bridge building animated to represent the forward momentum that he discusses.</p>
        <p>The decision to use a bridge over a chasm as the central image in his short animation comes from a Christian teaching in which the cross is the bridge between life and death, making it an understandable but different twist on a familiar principle.</p>
        <p>The assets for this project were drawn in <span class="bold">Adobe Photoshop</span> and animated in <span class="bold">After Effects</span>.</p>
