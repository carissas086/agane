        <h2 class="head-font arti-head">Little Red TeaHouse</h2>
        <div class="content-arti-clean flex">
            <div class="content-arti-copy">
                <p>"Little Red TeaHouse" was a brand belonging to Global Mobility Health, which sold brown rice tea imported from Singapore. This section of the project entailed creating packaging for various sizes and shapes of tea boxes, which were all printed and sold in various small craft fairs and markets over the course of 2018-2019.</p>
                <p>This project also entailed the creation of the brand's logo, and 2 other package variations for other lines of tea.</p>
                <p>As my first venture into package design, I found bringing all information into the page and keeping it readable the key challenge for me. From this project, I learned very quickly that there was a balance between necessary information and design that needed to be struck with package design, and that doing more with less is key.</p>
                <p>The core design elements for this project were drawn in <span class="bold">Adobe Illustrator</span> and laid out in <span class="bold">Adobe InDesign</span>.</p>
