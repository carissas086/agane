        <p>This project was a simple assignment exploring the 3D funtionality of Illustrator. I first created the pawn by creating a curve that would be used to generate the pawn piece through Illustrator's 3D engine. A glass material and a red hue was applied to give it a reddish-glass look. This piece was then duplicated and faded out using a white gradient box to create the soft glass reflection.</p>
        <p>As for the poster design, given the limitation of Illustrator's 3D engine, I went with a simple design, placing a pawn at the literal centre of the poster as a representation of a 'last man standing', and laying the competition information around the edges of the poster.</p>
