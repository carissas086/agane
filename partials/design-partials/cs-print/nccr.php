    <div class="content-arti-copy">
        <p>As I knew I would only be mocking up the package fronts, these were the only assets that would be required for the mockup. I exported these assets, and brought them into Adobe Dimension for the mockup. The background was applied of a basic coffee beans package model, and the decal positioned on the front. This mocked up model was duplicated, and the background and decal replaced for the second flavour.</p>
        <p>As an extension to this project, the mockup was exported as a Photoshop file, and further mocked up into a banner advert in Photoshop.</p>
        <p>This project was my first venture into product mockup through <span class="bold">Adobe Dimension</span>. Given the functionality to create variations easily, the challenge I chose to tackle with this project was to create a design that could easily support variations in colour and name, despite a relatively complex brand logo.</p>
        <p>This project was completed in <span class="bold">Adobe Illustrator</span> and <span class="bold">Dimension</span>, with the additional banner mocked up in <span class="bold">Adobe Photoshop</span>.</p>
