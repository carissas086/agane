    <div class="content-arti-copy">
        <p>I started this project by designing a front decal that would be able to accomodate various coffee roast names. This was important to me so that I would be able to consolidate the brand logo and key information into a single element. This would form the core feature of the packaging design.</p>
        <p>After these, I designed 2 package backgrounds for the 2 flavours of coffee that I would be mocking up. These were a light and a dark background utilising brand colours. This would afford me the additional challenge of dealing with varying contrasts on the bean packages, and ensure that this packaging design would be useable across any future package variation.</p>
