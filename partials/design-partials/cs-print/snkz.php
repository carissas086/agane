    <div class="content-arti-copy">
        <p>Knowing that the purpose of this project was to provide a mocked up end product, I started by doing a little research into common shoebox sizes for men's shoes. I then created various artboards in Photoshop to those dimensions, on which to create the box design.</p>
    </div>
</div>
<div class="content-arti-clean flex flip flex-end">
    <div class="content-arti-final framed">
        <a href="../images/cs_snkz-box-top.jpg"
            data-lightbox="snkz"
            data-title="Final shoebox lid top design. The background pattern was rendered with a scatter brush of tiny triangles to echo a school cafeteria." >
                <img src="../images/cs_snkz-box-top.jpg" alt="SNEAKZ shoebox box top design">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>Given the geometric nature of the brand logo and its bright colour palette, I chose to create a box with a geometric pattern that would wrap around the bottom of the box on top of a speckled triangle background to echo the floor of a school cafeteria. For the top of the box, on top of the same background texture was placed, simply, the brand logo.</p>
        <p>These assets were initially brought into Photoshop's 3D features. However, as they have been discontinued, this mockup was redone in <span class="bold">Adobe Dimension</span>, exported, and rendered again into the <a class="art-link" href="#snkz-top">banner image</a> in Photoshop.</p>
