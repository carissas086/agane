    <div class="content-arti-copy">
        <p>Following this, I had the URLs converted into QR codes, and chose base colours for each of the flavours. For the brown rice tea, I chose a deep red. As this would the be venture's first product, I decided that using the same red as was used in the logo would ensure that the venture's brand would be well represented.</p>
        <p>I chose simple design elements to build each packaging from, namely the cetnral graphic at a lowered opacity, and a white horizontal running across the whole packaging. This singular horizontal was chosen in order to offer grounding to the design, and to keep the brand perception simple.</p>
        <p>This design was then adapted to the various box and tin sizes. This was particularly challenging with the smaller boxes, as there were large blocks of information that needed to be included on each packaging, making keeping a simple design with a consistent amount of white space a challenge. This was largely overcome by concentrating information on the back of the packaging, and giving the front design room to breathe.</p>
