    <div class="content-arti-copy">
        <p>As a part of the startup for this venture, I was asked to design box and tin packaging at a number of sizes for a brown rice tea. I was provided with box dimensions from the manufacturer by way of outlines for each of the packaging types, nutrition information and ingredients, a URL that they wanted included via QR code, and a quick run-down of what the product being sold was.</p>
        <p>As the client wanted to market the tea for the health properties of each's core ingredients, I started by drawing and vectorising the core ingredients for each of the tea flavours that would eventually be sold. These were a stalk of rice, a sprig of rosemary, and a piece of ginger. Each would serve as the central design feature of each packaging size.</p>
    </div>
</div>
