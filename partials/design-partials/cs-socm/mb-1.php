    <div class="content-arti-copy">
        <p>Having gone to <a href="https://originsparkour.com/" class="art-link" target="_blank">Origins Parkour</a> Gym for several years, I was well acquainted with the imagery commonly used in their marketing. Being a relatively unique sport with a unique gym setup, I knew that I would want to feature these most prominently in the banners.</p>
        <p>I began by finding an image of the open gym space as the foundation for the long, thin banners. This would give viewers a clear picutre of what the gym offered: open space with structures to train off of. With that, I created a purple shape sloping upward to the right in order to lead the eye forward toward the call to action button on the far right. This would generate forward momentum, which was exactly what the gym slogan suggests to do.</p>
    </div>
</div>
<div class="content-arti-clean flex flip flex-end">
    <div class="content-arti-final framed flex">
        <a href="../images/cs_mb-origins-300.jpg"
            data-lightbox="origins"
            data-title="300px tall banner for Origins Parkour." >
                <img src="../images/cs_mb-origins-300.jpg" alt="Advertising banner for Origins Parkour.">
        </a>
    </div>
    <div class="content-arti-copy">
        <p>For the square and tall banners, I went with the image of one of the gym's coaches performing a precision jump. I chose this image because with the limited horizontal space, it was difficult to make the gym's sprawling nature work for the layout, and with that same slope shape theme, it would appear as those the cooach was flying over a structure. As this is what a lot of parkour tends to feel like, I thought this an appropriate feel for the banners.</p>
        <p>To keep the banner package consistent, I opted to include the slogan on these as well. However, instead of having the slogan as a string on text, I overlayed it over the image to give the image a hint of a motion graphics feel. I also opted to use the gym's work-marked logo instead of the round black seal as it offered better balance to the purple shapes.</p>
    </div>
