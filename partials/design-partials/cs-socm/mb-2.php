        <p>For this banner package, I wanted to utilise more of a neon, gaming aesthetic as per <a href="https://www.extra-life.org/" class="art-link" target="_blank">Extra Life</a>'s slogan: 'play games to change kids' health to change the future'. I overlaid a stock image of a game controller with images of children that Extra Life supported, and gamers playing games together as a base texture to build the banners on. This would hint at the mission statement of Extra Life, and create more interest as to what the organisation does.</p>
        <p>Through all banners, I had a dotted line pathing from 'Play to Raise', through Extra Life's logo, and ending at the call to action button 'For The Kids'. This would once again speak to extra life's mission statement. The angular line and the call to action button both hint toward the pixel art history in gaming.</p>
