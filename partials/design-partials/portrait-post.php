<h2 class="head-font arti-head">Process &mdash; Post-Processing</h2>
<div class="content-arti-clean flex flip flex-end">
    <div class="content-arti-copy">
        <p>The chosen shots were edited, masked, and then composited together to ensure that all the pieces were properly blocked out, and the image was well balanced. Once happy with this, I filled out the background and added a sense of space and scale to the piece by bringing in some environmental fog between each of the 6 shots. These were all hand painted in using Adobe Photoshop.</p>
        <p>Lighting adjustments were also made to tell a more dramatic story. As a musician who plays 'the computer', I wanted to lead the viewer's eye to Joseph reaching out of his computer into the real world, to the instrument he uses most to create music: not a piano keyboard, but a computer keyboard.</p>
