    <div class="content-arti-copy">
        <p>I leaned into these 2 sketches as they were both drawn out of the vague outline of the Pacific Ocean. The other element that I brought in was the dot, used to represent the company bringing the region together. This seemed apt to me as Singapore is sometimes colloquially referred to as the ‘Little Red Dot’ due to its small size on a map.</p>
        <p>The blue set leaning into organic shapes, starting as an exploration of a sort of avatar, represented by the dot and a triangular body. This was to harken to the company’s product allowing for the customisation to individual cultural needs. The green set leaned into a more abstract, geometric representation of the Pacific Rim, with the dot on the bottom to echo the idea of PacMan (a playful nod to the company’s main product: Pac Meta). This set of variations were designed to have a more technical feel, as the company’s proprietary platforms were their most important product.</p>
        <p>After further discussions with the client, I moved forward with their selected logo: green 3. The following task was therefore <span class="bold">to create additional variations for their products: Pac Metaverse, and Pac Meta</span>.</p>
