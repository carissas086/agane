        <p>After initial thumbnail sketched, I singled out 3 ideas that I felt had potential to develop further, keeping in mind that I would need to develop additional logo variations for the 2 products that the client wanted. Of those 3, 2 immediately stood out, from which I did a series of variations to show the client. There were laid out on a single canvas and numbered accordingly, as I would not be able to present the designs in person.</p>
