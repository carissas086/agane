<div class="content-arti-clean flex">
    <div class="content-arti-copy">
        <p>This set leaned into the centred dot as the key element, and playing with where negative space could be applied to generate variations for the company’s 2 products. Additionally, it was in this spread that I introduced colours, to show the client a variety of colour palettes that could be applied to their brand.</p>
        <p>From here, another conference call with the client, we discovered that the client missed the addition of the horizontals lines in the design, as without, the 3D tech aspect of their game development was lost. As such, a final set was developed with a mix of both lines and solid shapes, with the centred dot remaining the key design element, with the lines being used to generate negative space variations across the logo set.</p>
    </div>
