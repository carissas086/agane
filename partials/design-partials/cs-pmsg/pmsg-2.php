<h2 class="head-font arti-head">Logo Development</h2>
<div class="content-arti-clean flex">
    <div class="content-arti-copy">
        <p>A spread was developed for the client with variations of the logo they selected. The goal of this spread was to show the logo in all the ways that it could be shown:</p>
            <ul class="li-indent bullets">
                <li>just the graphic logo,</li>
                <li>just the wordmark, and</li>
                <li>a combination of both the graphic logo and wordmark.</li>
            </ul>
        <p>In developing variations, I wanted to ensure that the product logos were still consistent aesthetically with the company’s main logo. With this particular variation, the distinguishing feature was the set of lines running across the Pacific Rim shape. The ideology behind these lines were 2-fold:</p>
            <ol class="li-indent">
                <li>to visually establish the company’s identity as a tech company, and</li>
                <li>using lines of progressing width to show growth, and development (alluding to the company’s games being used for education).</li>
            </ol>
    </div>
</div>
