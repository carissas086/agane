<div class="content-arti-clean flex">
    <div class="content-arti-copy">
        <p>These forward moving lines were the elements that I used to tie these logos together. However, during a follow-up meeting with the client, it was expressed that they liked the image of the dot replaced in the centre of the logo, as though “stepping into the metaverse”. Given this feedback, a second set of variations was developed.</p>
    </div>
</div>
